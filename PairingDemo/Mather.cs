using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PairingDemo
{
    internal class Mather
    {
        //take in 2 doubles, add then return them
        public double adder(double x, double y)
        {
            return x + y;
        }
        //take in 2 doubles, subract then return
        public double subber(double x, double y)
        {
            return x - y;
        }
        //takes in 2 doubles, multiplys them then returns
        public double multiplyer(double x, double y)
        {
            return x * y;
        }
        //takes in 2 doubles, divider them then returns
        public double divider(double x, double y)
        {
            return x / y;
        }
    }
}
