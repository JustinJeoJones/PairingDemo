namespace PairingDemo
{
    internal class Mather
    {
        //take in 2 doubles, add then return them
        public double adder(double x, double y)
        {
            return x + y;
        }
        //take in 2 doubles, subract then return
        public double subber(double x, double y)
        {
            return x - y;
        }
    }
